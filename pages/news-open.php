                <p>A Prefeitura Municipal de Maracajá, por meio do Setor de Tributos, iniciou na segunda-feira, dia 25/04, a entrega nas residências e comércios, dos carnês do Imposto Predial e Territorial Urbano (IPTU) 2022.  Os 4 mil carnês estão sendo entregues pelo Fiscal da prefeitura, Francisco Demétrio da Rocha, com o auxílio do diretor de Indústria e Comércio, Valmir Pedro, em todo município. A conclusão da entrega deve acontecer em até 15 dias.</p>
                <p>Além da entrega domiciliar, a Administração também disponibiliza os carnês no site www.maracaja.sc.gov.br e no Setor de Tributos, no Paço Municipal.  Segundo a Fiscal de Tributos, Michelini Francisco, os contribuintes devem ficar atentos aos prazos para terem direito aos descontos.</p>
                <blockquote>
                    <p><strong>"O IPTU de Maracajá, tradicionalmente, tem vencimento para pagamento em parcela única com 40% de desconto, em 15 de maio, para contribuintes em dia com os cofres municipais. Quem tiver pendência com a Fazenda Municipal, mas pagar à vista tem direito a desconto de 30% e o prazo também é 15/05.  O parcelamento pode ser em até cinco vezes, desde que a parcela não seja inferior a R$ 50,00", detalha.</strong></p>
                </blockquote>
